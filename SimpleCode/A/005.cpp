// Variables & declaration
#include <iostream>
using std::cout;

int main() {
    cout << "005 Lesson: Variables & declaration\n\n";

    int a;
    a = 5;
    int b = -5;

    cout << "Before changing the variable: a = " << a << '\n';
    a = 10;
    cout << "After changing the variable: a = " << a << '\n';
    cout << "The variable: b = " << b << '\n';

    getchar();
    return 0;
}

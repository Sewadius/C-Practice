// Page 93 - операции отношения
#include <iostream>
using namespace std;

int main() {
    int number;

    cout << "Введите число: ";
    cin >> number;

    cout << "number < 10 равно " << (number < 10) << endl;
    cout << "number > 10 равно " << (number > 10) << endl;
    cout << "number == 10 равно " << (number == 10) << endl;

    return 0;
}

